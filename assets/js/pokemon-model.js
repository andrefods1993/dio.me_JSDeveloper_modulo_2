// Definição da classe Pokemon para representar um Pokémon com suas propriedades
class Pokemon {
	id; // Identificação única do Pokémon
	name; // Nome do Pokémon
	types = []; // Array de tipos do Pokémon
	mainType; // Tipo principal do Pokémon
	photo; // URL da imagem do Pokémon
	height = 0; // Altura do Pokémon (padrão inicial 0)
	weight = 0; // Peso do Pokémon (padrão inicial 0)
	hp = 0; // Pontos de Vida (HP) do Pokémon (padrão inicial 0)
	attack = 0; // Valor de ataque do Pokémon (padrão inicial 0)
	defense = 0; // Valor de defesa do Pokémon (padrão inicial 0)
	specialAttack = 0; // Valor de ataque especial do Pokémon (padrão inicial 0)
	specialDefense = 0; // Valor de defesa especial do Pokémon (padrão inicial 0)
	speed = 0; // Velocidade do Pokémon (padrão inicial 0)
}
